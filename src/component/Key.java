package component;

public class Key {

    private boolean keyRight;
    private boolean keyLeft;
    private boolean keySpace;
    private boolean keyJ;
    private boolean keyK;
    private boolean keyEnter;

    public boolean isKeyRight() {
        return keyRight;
    }

    public void setKeyRight(boolean keyRight) {
        this.keyRight = keyRight;
    }

    public boolean isKeyLeft() {
        return keyLeft;
    }

    public void setKeyLeft(boolean keyLeft) {
        this.keyLeft = keyLeft;
    }

    public boolean isKeySpace() {
        return keySpace;
    }

    public void setKeySpace(boolean keySpace) {
        this.keySpace = keySpace;
    }

    public boolean isKeyJ() {
        return keyJ;
    }

    public void setKeyJ(boolean keyJ) {
        this.keyJ = keyJ;
    }

    public boolean isKeyK() {
        return keyK;
    }

    public void setKeyK(boolean keyK) {
        this.keyK = keyK;
    }

    public boolean isKeyEnter() {
        return keyEnter;
    }

    public void setKeyEnter(boolean keyEnter) {
        this.keyEnter = keyEnter;
    }
}
